int		main_find(int *tab, int *x, int *y)
{
	if (*y >= 4)
	{
		*(tab + *x) = 0;
		if (*(tab + *x - 1) == 4)
		{
			*(tab + *x - 1) = 0;
			*y = *(tab + *x - 2) + 1;
			*x -= 2;
		}
		else
		{
			*y = *(tab + *x - 1) + 1;
			*x -= 1;
		}
		if (*x < 0)
			return (-1);
	}
	else
		*y += 1;
	return (0);
}
